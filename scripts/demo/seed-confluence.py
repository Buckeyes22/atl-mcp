def main() -> None:
    print(f"Seeding Confluence space {SPACE_KEY} ({SPACE_ID})...", flush=True)
    titles = list_existing_titles()
    print(f"  {len(titles)} existing pages (skipping these)", flush=True)

    # Title -> id, including pages we just created.
    title_to_id: dict[str, str] = dict(titles)

    for title, parent_title, body_fn in PAGES:
        if title in title_to_id:
            print(f"  exists: {title}")
            continue
        parent_id = HOMEPAGE_ID if parent_title is None else title_to_id.get(parent_title)
        if parent_id is None:
            print(f"  SKIP {title!r} (parent {parent_title!r} not found)", file=sys.stderr)
            continue
        try:
            body = body_fn()
            new_id = create_page(title, parent_id, body)
            title_to_id[title] = new_id
            print(f"  created: {title}  (id={new_id}, parent={parent_id})")
        except Exception as e:
            print(f"  FAILED {title!r}: {e}", file=sys.stderr)

    print("Done.", flush=True)


if __name__ == "__main__":
    main()

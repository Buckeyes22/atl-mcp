"""Seed the ACO Confluence space with the 28-page IA from the build plan.

Idempotent: skips a page if a page with the same title already exists in ACO.
Reads ATLASSIAN_* env vars from the process environment (load from .env first).
"""
from __future__ import annotations

import json
import os
import sys
from base64 import b64encode
from urllib.request import Request, urlopen
from urllib.error import HTTPError


SITE = os.environ["ATLASSIAN_SITE_URL"].rstrip("/")
EMAIL = os.environ["ATLASSIAN_EMAIL"]
TOKEN = os.environ["ATLASSIAN_API_TOKEN"]
SPACE_KEY = "ACO"
SPACE_ID = "2588674"
HOMEPAGE_ID = "2588787"

AUTH = "Basic " + b64encode(f"{EMAIL}:{TOKEN}".encode()).decode()


def api(method: str, path: str, body: dict | None = None) -> dict:
    url = f"{SITE}{path}"
    data = json.dumps(body).encode() if body is not None else None
    req = Request(url, data=data, method=method)
    req.add_header("Authorization", AUTH)
    req.add_header("Accept", "application/json")
    if data is not None:
        req.add_header("Content-Type", "application/json")
    try:
        with urlopen(req, timeout=30) as r:
            raw = r.read()
            return json.loads(raw) if raw else {}
    except HTTPError as e:
        body_text = e.read().decode("utf-8", "replace")
        print(f"HTTP {e.code} on {method} {path}: {body_text[:600]}", file=sys.stderr)
        raise


def list_existing_titles() -> dict[str, str]:
    """Return {title: page_id} for every page in the space."""
    titles: dict[str, str] = {}
    cursor: str | None = None
    while True:
        path = f"/wiki/api/v2/spaces/{SPACE_ID}/pages?limit=100"
        if cursor:
            path += f"&cursor={cursor}"
        res = api("GET", path)
        for p in res.get("results", []):
            titles[p["title"]] = p["id"]
        next_link = res.get("_links", {}).get("next")
        if not next_link:
            break
        # Cursor is a query param in the next link; extract.
        if "cursor=" in next_link:
            cursor = next_link.split("cursor=", 1)[1].split("&", 1)[0]
        else:
            break
    return titles


def create_page(title: str, parent_id: str, body_storage: str) -> str:
    payload = {
        "spaceId": SPACE_ID,
        "status": "current",
        "title": title,
        "parentId": parent_id,
        "body": {"representation": "storage", "value": body_storage},
    }
    res = api("POST", "/wiki/api/v2/pages", payload)
    return res["id"]



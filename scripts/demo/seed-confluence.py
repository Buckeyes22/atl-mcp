def audit_chain_design() -> str:
    return """
<h1>Audit Chain Design</h1>
<p><strong>TL;DR:</strong> Every state change appends an audit entry. Each entry is hash-linked to the previous one and signed with ed25519. The signing key is determined by a git-ref-versioned registry. The chain is verifiable offline.</p>

<h2>Entry shape</h2>
<table>
  <tr><th>Column</th><th>Purpose</th></tr>
  <tr><td><code>id</code></td><td>UUIDv7 (sortable) primary key</td></tr>
  <tr><td><code>actor</code></td><td>Who initiated the change &mdash; build-agent session, operator account, system</td></tr>
  <tr><td><code>operation</code></td><td>Verb + object (e.g. <code>jira.issue.create</code>)</td></tr>
  <tr><td><code>payload</code></td><td>Canonical JSON of the change inputs and outputs</td></tr>
  <tr><td><code>prev_hash</code></td><td>SHA-256 of the previous entry&rsquo;s canonical serialization (incl. its signature). NULL for genesis.</td></tr>
  <tr><td><code>payload_hash</code></td><td>SHA-256 of <code>payload</code></td></tr>
  <tr><td><code>chain_hash</code></td><td>SHA-256(<code>prev_hash || payload_hash</code>)</td></tr>
  <tr><td><code>signature</code></td><td>ed25519 signature over <code>chain_hash</code></td></tr>
  <tr><td><code>key_id</code></td><td>ID of the signing key (looked up in the git-ref registry)</td></tr>
  <tr><td><code>ts</code></td><td>Server-generated timestamp</td></tr>
</table>

<h2>Verifier</h2>
<p>An offline verifier walks the chain in order, recomputes each <code>chain_hash</code>, and validates each <code>signature</code> against the key registered for that <code>key_id</code> at the relevant git-ref commit. Any mismatch surfaces with the failing entry id and the specific failure mode (chain break vs signature invalid vs key unknown).</p>

<h2>Key registry</h2>
<p>The signing-key public-half is stored in a git ref. Why git: writes are atomic, rotations produce verifiable history, and ops can verify the registry against their fork without trusting the running server. Cost: rotation requires a commit + ref update, which is an operational ceremony.</p>
<p>Trade-off accepted in ADR-0005. Open questions about tag (immutable) vs branch (mutable) registry resolved in favor of branch + log-the-rotation-event.</p>

<h2>Genesis block</h2>
<p>The first entry has <code>prev_hash = NULL</code>. This is the chicken-and-egg case &mdash; documented intentionally. The verifier handles it as a special case (and notes &ldquo;chain length 1, genesis only&rdquo; in its output).</p>

<h2>What&rsquo;s out of scope (v1)</h2>
<ul>
  <li>Per-tenant key isolation. Single-tenant only; multi-tenant runway documented in v6 &sect;7.3 and tracked as <strong>PCO-51</strong>.</li>
  <li>Master encryption-key rotation for the token store. Tracked as <strong>PCO-57</strong>.</li>
</ul>

<h2>Linked artifacts</h2>
<ul>
  <li>Spec: v6 &sect;30.1</li>
  <li>ADR: ADR-0005 (audit signing pipeline)</li>
  <li>Code: <code>src/storage/schema/auditEntries.ts</code>, <code>src/security/policyDecisionLayer.ts</code></li>
  <li>Jira: PCO-9 (flagship: implement hash linkage), PCO-48 (key registry), PCO-49 (verifier CLI)</li>
</ul>
"""


